/**************************************************************************************************
 * Check if the input is an odd number
 *************************************************************************************************/
const isOdd = function (input) { return input & 1; };

/**************************************************************************************************
 * Check if the input is an even number
 *************************************************************************************************/
const isEven = function (input) { return !(input & 1); };

